    owner_user = User.find_by(role: owner_role)
    return Rails.logger.error('Owner user not found. Aborting search.') unless owner_user

    @owner_token = fetch_oauth_token(owner_user.id)
    return Rails.logger.error('Owner access token not found. Aborting search.') unless @owner_token

    Rails.logger.info "Starting search for #{followers.length} accounts"

    followers.each_with_index do |handle, index|
      Rails.logger.info "Searching account #{index + 1}/#{followers.length}: #{handle}"
      
      account_id = search_target_account_id(handle, @owner_token)
      
      if account_id
        @found_accounts << { handle: handle, account_id: account_id }
        Rails.logger.info " Found: #{handle} (ID: #{account_id})"
      else
        @missing_accounts << handle
        Rails.logger.error " Not found: #{handle}"
      end
    end
    
    output_search_results
  end

  private

  def output_search_results
    Rails.logger.info "="*80
    Rails.logger.info "SEARCH COMPLETED"
    Rails.logger.info "="*80
    
    Rails.logger.info "Total accounts searched: #{@found_accounts.length + @missing_accounts.length}"
    Rails.logger.info "Found accounts: #{@found_accounts.length}"
    Rails.logger.info "Missing accounts: #{@missing_accounts.length}"
    Rails.logger.info "-" * 80
    
    if @found_accounts.any?
      Rails.logger.info " Found accounts:"
      @found_accounts.each_with_index do |account, index|
        Rails.logger.info "#{index + 1}. #{account[:handle]} (ID: #{account[:account_id]})"
      end
      Rails.logger.info "-" * 80
    end
    
    if @missing_accounts.any?
      Rails.logger.info " Missing accounts:"
      @missing_accounts.each_with_index do |handle, index|
        Rails.logger.info "#{index + 1}. #{handle}"
      end
    end
    
    Rails.logger.info "="*80
  end

  def search_target_account_id(query, owner_token)
    retries = 5
    result = nil
    while retries >= 0
      result = ContributorSearchService.new(
        query,
        url: ENV['MASTODON_INSTANCE_URL'],
        token: owner_token
      ).call
      if result.any?
        return result.last['id']
      end
      
      # Sleep for 1 second before next attempt (except on last iteration)
      sleep(1) if retries > 0
      retries -= 1
    end
    nil
  end

  def fetch_oauth_token(user_id)
    token_service = GenerateAdminAccessTokenService.new(user_id)
    token_service.call
  end
